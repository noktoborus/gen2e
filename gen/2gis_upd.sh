[Desktop Entry]
Name=2GIS
Comment=Browse the city
GenericName=2GIS
Exec=/bin/sh -c "cd '$di'; wine '$pa'"
Icon=/usr/share/icons/d1f7_grym.png
Terminal=false
StartupNotify=true
Type=Application
Categories=Office;

# vi:set encoding=UTF-8:
!

